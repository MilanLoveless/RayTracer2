#include "Material.h"

namespace ThreeD
{
	_MATERIAL::_MATERIAL() {}
/////////////////////////////////////////////////////////////////////////////////////////////////////
	_MATERIAL::_MATERIAL(const _MATERIAL &mat) {}
/////////////////////////////////////////////////////////////////////////////////////////////////////
	_MATERIAL& _MATERIAL::operator= (const _MATERIAL &mat)
	{
		if(this == &mat)
			return (*this);
		return (*this);
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////
	_MATERIAL::~_MATERIAL() {}
/////////////////////////////////////////////////////////////////////////////////////////////////////
	_COLOR4F _MATERIAL::_Shade(_SHADEREC &sr)
	{
		return (_COLOR4F(0.0));
	}
}