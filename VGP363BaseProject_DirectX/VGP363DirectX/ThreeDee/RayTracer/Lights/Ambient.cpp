// Ambient.cpp
////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "Ambient.h"

namespace ThreeD
{
////////////////////////////////////////////////////////////////////////////////////////////////////////////////	
	_AMBIENT::_AMBIENT()
		:_LIGHT()
	{
		ls = 0.1;
		color = _COLOR4F(1.0);
	}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	_AMBIENT::~_AMBIENT() {}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	_VERTEX4F _AMBIENT::_GetDirection(_SHADEREC &sr)
	{
		return (_VERTEX4F(0.0, 0.0, 0.0, 0.0));
	}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	_COLOR4F _AMBIENT::_L(_SHADEREC &sr)
	{
		return (color * ls);
	}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	bool _AMBIENT::_InShadow(const _RAY &ray, const _SHADEREC &sr)
	{
		return false;
	}
}